/** \brief verifica si un array de caracteres es solo un numero flotante
 *
 * \param array de cadena de caracteres a verificar
 * \return 1 si es flotante -1 si no lo es
 *
 */
int itsFloatNumber (char []);


/** \brief verifica si un array de cadena de caracteres ingresado es solo un numero
 *
 * \param char array de cadena de caracteres a verificar
 * \return int 1 si es numero -1 si no lo es
 *
 */
int itsNumber(char []);


/** \brief verifica si un array de cadena de caracteres ingresado es solo de letras y espacios
 *
 * \param [] char array de cadena de caracteres a verificar
 * \return int 1 si es array de cadena de caracteres solo de letras y espacios, -1 si no lo es
 *
 */
int itsStringJustLetters(char []);


/** \brief verifica si el array de cadena de caracteres ingresado es un numero de telefono o celular
 *
 * \param [] char array de cadena de caracteres a verificar
 * \return int 1 si es numero de telefono -1 si no lo es
 *
 */
int itsPhoneNumber(char []);


